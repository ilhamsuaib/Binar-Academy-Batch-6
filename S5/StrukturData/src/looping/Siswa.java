package looping;

public class Siswa {

    private String nama;
    private int nis;

    public Siswa(String nama, int nis) {
        this.nama = nama;
        this.nis = nis;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getNis() {
        return nis;
    }

    public void setNis(int nis) {
        this.nis = nis;
    }
}
